# "출력"이라고 하려면, 함수의 실행이 끝날 때 결과 값이 "출력"되는 것을 의미한다.
# 그 값을 다른 곳(함수, 변수 등등)에서 받아볼 수 있게, 사용할 수 있게 해줄 것이다.

# 예시를 들어보자.
