def num_add2(num1, num2):
    res = num1 + num2
    return res  # 함수의 결과 값을 반환 시켜준다.

result = num_add2(1,4)  # 뛰어나온 값(반환된 값)을 변수에 할당

print(result)   # 출력(화면에)