# 위 코드는 cond_int의 값이 10이랑 같아지거나 커지지 않는 이상 계속 True일 것이기 때문에 무한히 인사를 하게 되는 것이다.
# 멈춰야 하니 cond_int에 영향을 주는 코드를 추가해서 다시 작성해보자.

