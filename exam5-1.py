# 무한히 반복하는 코드도 작성할 수 있다.
# while (조건) 에서 "조건"부분이 참일 경우 계속 반복 되므로, 조건 부분은 True로 고정 시켜주면 된다.
# while (True):
# 하지만 무한 반복문을 사용했을 때에는 반드시 그 반복문을 종료 시켜줄 조건문이 반복문 내부에 있어야한다.
# 다음 예시를 보자

